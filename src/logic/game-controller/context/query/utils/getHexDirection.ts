import { Coordinates } from '../../../../../types';

const vectors: Coordinates[] = [
  { q: 1, r: 0, s: -1 },
  { q: 1, r: -1, s: 0 },
  { q: 0, r: -1, s: 1 },
  { q: -1, r: 0, s: 1 },
  { q: -1, r: 1, s: 0 },
  { q: 0, r: 1, s: -1 },
];

export function getHexDirection(direction: string | number) {
  return vectors[direction];
}
